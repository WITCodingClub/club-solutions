package _2023.day08;

public class Coordinate
{
	private String x;
	private String y;
	
	public Coordinate(String x, String y)
	{
		this.x = x;
		this.y = y;
	}
	
	public String getX()
	{
		return x;
	}
	
	public String getY()
	{
		return y;
	}
	
	public void setX(String x)
	{
		this.x = x;
	}
	
	public void setY(String y)
	{
		this.y = y;
	}
}
